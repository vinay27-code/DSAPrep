#include<iostream>
using namespace std;
int main(){

    //for loop is used to execute a block of code for a specified number of times. It is a part of control statements in C++.
    //lets print "Hello world" 10 times using for loop.
    for(int i=0; i<10; i++){
        cout<<"Hello world"<<endl;
    }


    //lets print the numbers from 1 to 10 using for loop.
    for(int i=1; i<=10; i++){
        cout<<i<<" ";
    }
    return 0;
}