#include<iostream>
using namespace std;

int main(){
    //lets print the alphabets from A to Z using for loop.
    for(char i='A'; i<='Z'; i++){
        cout<<i<<" ";
    }

    cout<<endl;
    //lets print the alphabets from a to z using for loop.
    for(char i='a';i<='z';i++){
        cout<<i<<" ";
    }

    return 0;
}